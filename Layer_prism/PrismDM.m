function [g]=PrismDM(x0,rho,w,x,z2)
G=6.67*10^(-11);
 
 z1=0.01;
%  z2=2100;
 t1=z1.^2+(((x-x0)+(w/2)).^2);
    t2=z2.^2+(((x-x0)+(w/2)).^2);
    t3=z1.^2+(((x-x0)-(w/2)).^2);
    t4=z2.^2+(((x-x0)-(w/2)).^2);

    f1=acot(z2./((x-x0)+w/2))-acot(z2./((x-x0)-w/2));
    f2=acot(z1./((x-x0)+w/2))-acot(z1./((x-x0)-w/2));
    f3=((x-x0)./2).*log((t3.*t2)./(t1.*t4));
    f4=(w/4).*log((t4.*t2)./(t3.*t1));

    f=(z2.*f1)-(z1.*f2)+f3+f4;
    g=f.*2.*G.*rho.*100000;
end